#include "libft.h"

char	*ft_strdup(char *src)
{
	int		i;
	char	*dest;
	int		srclen;

	srclen = ft_strlen(src)+1;
	dest = malloc(srclen);
	if (!dest)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

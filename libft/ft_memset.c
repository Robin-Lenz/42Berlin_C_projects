#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned int	i;
	char			*p;

	p = s;
	i = 0;
	while (i < n)
	{
		*p = c;
		i++;
		p++;
	}
	return (s);
}
